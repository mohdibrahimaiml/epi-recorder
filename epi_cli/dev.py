def _run_fast_demo(
    *,
    demo_script: Path,
    no_run: bool,
    no_verify: bool,
    no_browser: bool,
    force_script: bool,
) -> Path | None:
    """Record → verify → view without gateway. Returns path to .epi or None."""
    console.print()
    console.print(Panel.fit(
        "[bold cyan]epi demo[/bold cyan] — record · seal · verify · view\n"
        "[dim]No API key. No server. One portable .epi file.[/dim]",
        border_style="cyan",
    ))
    console.print()

    # Always refresh generated script unless user keeps a custom path
    if force_script or not demo_script.exists() or demo_script.name == "demo_refund.py":
        # Overwrite stock demo so users always get the polished script
        if demo_script.name == "demo_refund.py" or not demo_script.exists():
            demo_script.write_text(_DEMO_SCRIPT, encoding="utf-8")
            console.print(f"  [green]✓[/green] Demo script ready     [cyan]{demo_script.name}[/cyan]")
        else:
            console.print(f"  [dim]Using existing {demo_script.name}[/dim]")
    else:
        console.print(f"  [dim]Using existing {demo_script.name}[/dim]")

    # Ensure default key exists so the artifact signs cleanly
    try:
        from epi_core.keys import KeyManager
        km = KeyManager()
        try:
            km.generate_keypair("default", overwrite=False)
        except Exception:
            pass
        _ensure_default_key_trusted()
    except Exception:
        pass

    epi_path: Optional[Path] = None
    if not no_run:
        console.print("  [dim]Recording refund agent (mock LLM if no OPENAI_API_KEY)…[/dim]")
        run_env = os.environ.copy()
        run_env.setdefault("EPI_NOTARIZE", "0")  # fast offline demo
        ok = _run_demo_script(demo_script, env=run_env)
        if not ok:
            console.print("  [yellow]![/yellow] Demo script exited non-zero — checking for artifact anyway")
        time.sleep(0.2)
        epi_path = _find_demo_epi("demo_refund")
        if epi_path is None:
            # Direct path fallback
            candidate = Path("epi-recordings") / "demo_refund.epi"
            if candidate.exists():
                epi_path = candidate
        if epi_path is None:
            console.print("  [red]✗[/red] Could not find demo_refund.epi after the run")
            return None
        size_kb = epi_path.stat().st_size // 1024 + 1
        console.print(f"  [green]✓[/green] Sealed artifact        [cyan]{epi_path}[/cyan]  ({size_kb} KB)")
    else:
        epi_path = _find_demo_epi("demo_refund") or (Path("epi-recordings") / "demo_refund.epi")
        if not epi_path.exists():
            console.print("  [red]✗[/red] --no-run set but no demo_refund.epi found")
            return None

    if not no_verify and epi_path is not None:
        console.print("  [dim]Verifying offline…[/dim]")
        result = subprocess.run(
            [sys.executable, "-m", "epi_cli", "verify", "--json", str(epi_path)],
            capture_output=True,
            text=True,
            timeout=30,
        )
        integrity = signature = "?"
        if result.returncode == 0 and result.stdout.strip():
            try:
                import json as _json
                report = _json.loads(result.stdout)
                facts = report.get("facts") or {}
                integrity = "PASS" if facts.get("integrity_ok") else "FAIL"
                signature = "PASS" if facts.get("signature_valid") else "FAIL"
            except Exception:
                integrity = signature = "OK" if result.returncode == 0 else "FAIL"
        else:
            integrity = signature = "FAIL" if result.returncode else "OK"
            if result.stderr:
                console.print(f"  [dim]{result.stderr.strip()[:200]}[/dim]")

        ok_color = "green" if integrity == "PASS" and signature == "PASS" else "yellow"
        console.print(f"  [{ok_color}]✓[/{ok_color}] Offline verify         integrity={integrity}  signature={signature}")

        # Prove redaction worked on the intentional demo secret
        try:
            steps = EPIContainer.read_steps(epi_path)
            blob = str(steps)
            if "sk-" + ("d" * 40) not in blob and "REDACTED" in blob:
                console.print("  [green]✓[/green] Secrets redacted       demo API key not present in steps")
            elif "sk-" + ("d" * 40) not in blob:
                console.print("  [green]✓[/green] Secrets redacted       no raw demo key in steps")
        except Exception:
            pass

    if not no_browser and epi_path is not None:
        console.print("  [dim]Opening viewer…[/dim]")
        view_rc = subprocess.run(
            [sys.executable, "-m", "epi_cli", "view", "--native", str(epi_path)],
            timeout=30,
        )
        if view_rc.returncode == 0:
            console.print("  [green]✓[/green] Viewer opened")
        else:
            # Fallback: extract viewer.html and open file://
            try:
                import tempfile
                import zipfile
                td = Path(tempfile.mkdtemp(prefix="epi_demo_view_"))
                with EPIContainer._payload_zip_path(epi_path) as payload:
                    with zipfile.ZipFile(payload) as zf:
                        zf.extract("viewer.html", path=td)
                uri = (td / "viewer.html").resolve().as_uri()
                webbrowser.open(uri)
                console.print(f"  [green]✓[/green] Viewer opened          {uri}")
            except Exception as exc:
                console.print(f"  [yellow]![/yellow] Could not open viewer automatically: {exc}")
                console.print(f"  [dim]Run: epi view {epi_path}[/dim]")

    try:
        telemetry_core.record_first_use()
        telemetry_core.track_event(
            "epi.demo.completed",
            {"command": "demo", "source": "cli", "success": True, "mode": "fast"},
        )
    except Exception:
        pass

    console.print()
    console.print(Panel.fit(
        f"[bold green]Done.[/bold green] Your evidence file is ready.\n\n"
        f"  [cyan]{epi_path}[/cyan]\n\n"
        f"Re-check anytime:\n"
        f"  [white]epi verify {epi_path}[/white]\n"
        f"  [white]epi view {epi_path}[/white]\n\n"
        f"[dim]Optional team inbox UI: epi demo --review[/dim]",
        title="EPI demo",
        border_style="green",
    ))
    console.print()
    return epi_path


@app.callback(invoke_without_command=True)
def dev(
    ctx: typer.Context,
    port: int = typer.Option(8787, "--port", "-p", help="Local review workspace port (only with --review)."),
    storage_dir: Path = typer.Option(Path("./evidence_vault"), "--storage-dir", help="Local case storage directory (only with --review)."),
    no_browser: bool = typer.Option(False, "--no-browser", help="Skip opening the browser."),
    demo_script: Path = typer.Option(Path("demo_refund.py"), "--demo-script", help="Path to demo script (auto-generated if missing)."),
    no_run: bool = typer.Option(False, "--no-run", help="Skip running the demo script."),
    no_verify: bool = typer.Option(False, "--no-verify", help="Skip offline verify step."),
    review: bool = typer.Option(
        False,
        "--review",
        help="Also start the local team review workspace (requires epi-recorder[gateway]).",
    ),
):
    """
    Fastest EPI loop: record a sample agent, seal a .epi, verify offline, open the viewer.

    No API key, no internet, no config required.
    Use --review for the optional gateway inbox UI.
    """
    if ctx.invoked_subcommand is not None:
        return

    demo_script = demo_script.resolve() if demo_script.is_absolute() else (Path.cwd() / demo_script)

    # ── Default: magic fast path (no gateway) ─────────────────────────────
    if not review:
        epi = _run_fast_demo(
            demo_script=demo_script,
            no_run=no_run,
            no_verify=no_verify,
            no_browser=no_browser,
            force_script=True,
        )
        if epi is None:
            raise typer.Exit(1)
        return

    # ── Optional: gateway review workspace ────────────────────────────────
    from epi_cli._shared import require_extra

    require_extra("gateway", "uvicorn", "epi demo --review")
    require_extra("gateway", "fastapi", "epi demo --review")

    storage_dir = storage_dir.resolve()

    # Run fast capture first so we always have a live .epi
    epi_path = _run_fast_demo(
        demo_script=demo_script,
        no_run=no_run,
        no_verify=no_verify,
        no_browser=True,  # gateway UI opens instead
        force_script=True,
    )

    console.print()
    console.print("[dim]Starting optional review workspace (--review)…[/dim]")

    actual_port = _find_free_port(port)
    if actual_port != port:
        console.print(f"[yellow]  [!] Port {port} busy — using {actual_port}[/yellow]")

    gateway_url = f"http://127.0.0.1:{actual_port}"
    gateway_env = os.environ.copy()
    gateway_env["EPI_GATEWAY_STORAGE_DIR"] = str(storage_dir)
    gateway_env["EPI_GATEWAY_RETENTION_MODE"] = "full_content"

    gateway_proc = subprocess.Popen(
        [sys.executable, "-m", "uvicorn", "epi_gateway.main:app",
         "--host", "127.0.0.1", "--port", str(actual_port), "--log-level", "warning"],
        env=gateway_env,
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
    )

    if not _wait_for_port("127.0.0.1", actual_port, timeout=20.0):
        console.print("[red]  [FAIL] The local review workspace did not start.[/red]")
        gateway_proc.terminate()
        raise typer.Exit(1)

    console.print(f"[green]  [OK] Local review workspace ready -> {gateway_url}[/green]")

    case_id: Optional[str] = None
    case_source = "none"
    if epi_path and epi_path.exists():
        case_id = _ingest_epi_into_gateway(epi_path, storage_dir)
        if case_id:
            case_source = "live"
    if not case_id:
        case_id = _seed_simulated_case(storage_dir)
        case_source = "simulated"

    if not no_browser:
        time.sleep(0.3)
        webbrowser.open(gateway_url)
        console.print(f"[green]  [OK] Browser opened -> {gateway_url}[/green]")

    source_label = (
        "[green]Live captured[/green]" if case_source == "live"
        else "[yellow]DEMO - simulated[/yellow]"
    )
    console.print()
    console.print(f"  Case: Refund ORD-9001 ($900)  ({source_label})")
    console.print(f"  Storage: [cyan]{storage_dir}[/cyan]")
    console.print("  Press [bold]Ctrl+C[/bold] to stop the review workspace.")
    console.print()

    try:
        while True:
            if gateway_proc.poll() is not None:
                console.print("[red][!] The local review workspace exited unexpectedly.[/red]")
                break
            time.sleep(1.0)
    except KeyboardInterrupt:
        pass
    finally:
        console.print()
        console.print("[dim]Stopping the local review workspace...[/dim]")
        try:
            gateway_proc.terminate()
            gateway_proc.wait(timeout=5.0)
        except Exception:
            gateway_proc.kill()
        console.print("[green][OK][/green] Stopped.")
