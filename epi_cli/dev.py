app = typer.Typer(help="Zero-friction developer onboarding for EPI.")


@app.callback(invoke_without_command=True)
def dev(
    ctx: typer.Context,
    port: int = typer.Option(8787, "--port", "-p", help="Local review workspace port."),
    storage_dir: Path = typer.Option(Path("./evidence_vault"), "--storage-dir", help="Local case storage directory."),
    no_browser: bool = typer.Option(False, "--no-browser", help="Skip opening the browser."),
    demo_script: Path = typer.Option(Path("demo_refund.py"), "--demo-script", help="Path to demo script (auto-generated if missing)."),
    no_run: bool = typer.Option(False, "--no-run", help="Skip running the demo script."),
    no_verify: bool = typer.Option(False, "--no-verify", help="Skip the interactive export + verify step."),
):
    """
    Start the fastest EPI repro loop: sample run, browser case view, export, verify.

    No API key, no internet, no config required.
    """
    if ctx.invoked_subcommand is not None:
        return

    storage_dir = storage_dir.resolve()
    demo_script = demo_script.resolve()

    console.print()
    console.print(Panel.fit(
        "[bold cyan]epi demo[/bold cyan] — portable repro in one command\n"
        "[dim]Real capture. Honest labeling. Open, share, verify.[/dim]",
        border_style="cyan",
    ))
    console.print()

    # ── [0] Generate demo script ──────────────────────────────────────────
    if not demo_script.exists():
        demo_script.write_text(_DEMO_SCRIPT, encoding="utf-8")
        console.print(f"[green]  [+][/green] Generated {demo_script.name}")
    else:
        console.print(f"[dim]  [-] Using existing {demo_script.name}[/dim]")

    # ── [1] Start gateway ─────────────────────────────────────────────────
    actual_port = _find_free_port(port)
    if actual_port != port:
        console.print(f"[yellow]  [!] Port {port} busy — using {actual_port}[/yellow]")

    gateway_url = f"http://127.0.0.1:{actual_port}"

    console.print(f"[dim]  Starting the local review workspace on {gateway_url}...[/dim]")

    gateway_env = os.environ.copy()
    gateway_env["EPI_GATEWAY_STORAGE_DIR"] = str(storage_dir)
    gateway_env["EPI_GATEWAY_RETENTION_MODE"] = "full_content"

    gateway_proc = subprocess.Popen(
        [sys.executable, "-m", "uvicorn", "epi_gateway.main:app",
         "--host", "127.0.0.1", "--port", str(actual_port), "--log-level", "warning"],
        env=gateway_env,
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
    )

    if not _wait_for_port("127.0.0.1", actual_port, timeout=20.0):
        console.print("[red]  [FAIL] The local review workspace did not start.[/red]")
        gateway_proc.terminate()
        raise typer.Exit(1)

    console.print(f"[green]  [OK] Local review workspace ready -> {gateway_url}[/green]")

    # ── [2] Run demo script ───────────────────────────────────────────────
    case_id: Optional[str] = None
    case_source = "none"

    if not no_run:
        console.print()
        console.print("[dim]  Running demo_refund.py (capturing one sample AI run)...[/dim]")
        console.print()

        run_env = os.environ.copy()
        run_env["EPI_QUIET"] = "0"
        # Write epi file to known location relative to cwd
        run_env["EPI_RECORDINGS_DIR"] = str(Path.cwd() / "epi-recordings")

        script_ok = _run_demo_script(demo_script, env=run_env)

        console.print()
        if script_ok:
            console.print("[green]  [OK] Demo script completed[/green]")
        else:
            console.print("[yellow]  [!] Demo script exited non-zero[/yellow]")

        # ── [3] Ingest the live artifact ──────────────────────────────────
        # Small delay so the .epi file is fully written
        time.sleep(0.5)
        epi_path = _find_demo_epi("demo_refund")

        if epi_path:
            console.print(f"[dim]  Captured: {epi_path.name} ({epi_path.stat().st_size // 1024 + 1} KB)[/dim]")
            console.print("[dim]  Building the review case file...[/dim]")
            case_id = _ingest_epi_into_gateway(epi_path, storage_dir)
            if case_id:
                case_source = "live"
                console.print(f"[green]  [OK] Live captured case ready[/green]")
        else:
            console.print("[yellow]  [!] Could not find demo_refund.epi[/yellow]")

    # ── [4] Fallback: seed simulated case ─────────────────────────────────
    if not case_id:
        console.print("[dim]  Seeding simulated demo case as fallback...[/dim]")
        case_id = _seed_simulated_case(storage_dir)
        case_source = "simulated"
        console.print("[yellow]  [DEMO] Simulated case added (not a real capture)[/yellow]")

    # ── [5] Open browser ──────────────────────────────────────────────────
    console.print()
    if not no_browser:
        time.sleep(0.3)
        webbrowser.open(gateway_url)
        console.print(f"[green]  [OK] Browser opened -> {gateway_url}[/green]")

    # ── [6] Pipeline summary ──────────────────────────────────────────────
    source_label = (
        "[green]Live captured[/green]" if case_source == "live"
        else "[yellow]DEMO - simulated[/yellow]"
    )

    console.print()
    console.rule("[bold cyan]Demo ready[/bold cyan]")
    console.print()
    console.print(f"  [green]✔[/green] Review workspace ready   {gateway_url}")
    if case_source == "live":
        console.print(f"  [green]✔[/green] Decision captured         demo_refund.epi")
    console.print(f"  [green]✔[/green] Case in inbox             Refund ORD-9001 ($900)  ({source_label})")
    console.print(f"  [green]✔[/green] Browser opened            Inbox ready")
    console.print()
    console.print("  [bold]In your browser:[/bold]")
    console.print("    1. Click the case → [bold]Refund ORD-9001[/bold]")
    console.print("    2. Read the flagged decision and the step-by-step trail")
    console.print("    3. Click [green]Approve[/green], [red]Reject[/red], or export the case file")
    console.print()
    console.rule()

    # ── [7] Interactive export + verify ───────────────────────────────────
    if not no_verify:
        console.print()
        console.print("  [bold]Proof step[/bold] — export this case as a tamper-evident bug report artifact.")
        console.print("  This is what you attach to a GitHub issue, PR, Slack thread, or downstream handoff.")
        console.print()
        try:
            input("  Press Enter to export & verify  (Ctrl+C to skip) → ")
        except (EOFError, KeyboardInterrupt):
            console.print()
            console.print("  [dim]Skipped. Run manually:[/dim]")
            console.print(f"    epi gateway export --case-id {case_id} --out refund_case.epi")
            console.print("    epi verify refund_case.epi")
        else:
            exported = _auto_export_and_verify(storage_dir, case_id)
            if exported:
                console.print()
                console.print(f"  [bold green]✔ Case file verified: {exported.name}[/bold green]")
                console.print("  [dim]This file is cryptographically tamper-evident.[/dim]")
                console.print("  [dim]Attach it. Store it. Verify it anywhere.[/dim]")
            console.print()
            console.print("  [dim]To re-verify later:[/dim]")
            console.print("  [dim]  epi verify refund_case.epi[/dim]")

    # ── [8] Persistence note ──────────────────────────────────────────────
    console.print()
    console.rule()
    console.print()
    console.print(f"  Cases stored at:   [cyan]{storage_dir}[/cyan]")
    console.print("  Restart [bold]epi demo[/bold] to continue where you left off.")
    console.print()
    console.print("  Press [bold]Ctrl+C[/bold] to close the local review workspace.")
    console.print()

    # ── [9] Keep gateway alive ────────────────────────────────────────────
    try:
        while True:
            if gateway_proc.poll() is not None:
                console.print("[red][!] The local review workspace exited unexpectedly.[/red]")
                break
            time.sleep(1.0)
    except KeyboardInterrupt:
        pass
    finally:
        console.print()
        console.print("[dim]Stopping the local review workspace...[/dim]")
        try:
            gateway_proc.terminate()
            gateway_proc.wait(timeout=5.0)
        except Exception:
            gateway_proc.kill()
        console.print("[green][OK][/green] Stopped.")
