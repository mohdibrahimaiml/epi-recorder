def _find_free_port(preferred: int = 8787) -> int:
    try:
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.bind(("127.0.0.1", preferred))
            return preferred
    except OSError:
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.bind(("127.0.0.1", 0))
            return s.getsockname()[1]


def _wait_for_port(host: str, port: int, timeout: float = 20.0) -> bool:
    deadline = time.time() + timeout
    while time.time() < deadline:
        try:
            with socket.create_connection((host, port), timeout=0.5):
                return True
        except OSError:
            time.sleep(0.3)
    return False


def _find_demo_epi(stem: str = "demo_refund") -> Optional[Path]:
    """Find the most recently written .epi file matching stem."""
    candidates: list[Path] = []
    for search_dir in (Path("epi-recordings"), Path(".")):
        candidates.extend(search_dir.glob(f"{stem}*.epi"))
    if not candidates:
        return None
    return max(candidates, key=lambda p: p.stat().st_mtime)


def _ingest_epi_into_gateway(epi_path: Path, storage_dir: Path) -> Optional[str]:
    """
    Read a local .epi case file and push it into the gateway case store.
    Returns the case_id on success, None on failure.
    Labels the case "[Live captured]" so it's visually distinct from seeded demo data.
    """
    try:
        from epi_gateway.worker import EvidenceWorker

        try:
            EPIContainer.detect_container_format(epi_path)
        except Exception:
            return None

        try:
            manifest_payload = EPIContainer.read_member_json(epi_path, "manifest.json")
            manifest: dict = manifest_payload if isinstance(manifest_payload, dict) else {}
        except Exception:
            manifest = {}

        steps = EPIContainer.read_steps(epi_path)

        case_id = (
            manifest.get("recording_id")
            or manifest.get("id")
            or f"live-{uuid.uuid4().hex[:8]}"
        )
        created_at = manifest.get("created_at") or datetime.now(timezone.utc).isoformat()

        worker = EvidenceWorker(storage_dir=storage_dir)
        payload = {
            "id": case_id,
            "title": "[Live captured] Refund approval investigation - ORD-9001",
            "created_at": created_at,
            "manifest": manifest,
            "steps": steps,
            "analysis": {
                "review_required": True,
                "summary": (
                    "Agent approved a $900 refund for Gold customer ORD-9001. "
                    "Human review is still required because the amount exceeds the $500 threshold. "
                    "Confidence: 94%."
                ),
            },
        }
        result = worker.upsert_case_payload(payload)
        return result.get("id") or case_id

    except Exception as exc:
        console.print(f"[yellow][!][/yellow] Could not ingest the case file: {exc}")
        return None


