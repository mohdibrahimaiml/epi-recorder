def _run_demo_script(script_path: Path, env: dict) -> bool:
    """Run the demo script. Returns True if it exited with code 0."""
    try:
        result = subprocess.run(
            [sys.executable, str(script_path)],
            env=env,
            timeout=60,
        )
        return result.returncode == 0
    except subprocess.TimeoutExpired:
        console.print("[yellow][!][/yellow] Demo script timed out (60s)")
        return False
    except Exception as exc:
        console.print(f"[yellow][!][/yellow] Demo script error: {exc}")
        return False


def _auto_export_and_verify(storage_dir: Path, case_id: str) -> Optional[Path]:
    """
    Export the case to a .epi case file and run epi verify on it.
    Prints the full verify output so the user sees the signed proof.
    Returns the exported path or None on failure.
    """
    from epi_gateway.worker import EvidenceWorker
    from epi_gateway.main import _build_gateway_signer, _build_settings_from_env

    out_path = Path("refund_case.epi").resolve()

    console.print()
    console.print("[dim]Exporting the case file...[/dim]")

    worker = EvidenceWorker(storage_dir=storage_dir)

    try:
        settings = _build_settings_from_env()
        signer = _build_gateway_signer(settings)
    except Exception:
        signer = None

    try:
        worker.export_case(case_id, out_path, signer_function=signer)
        signed = signer is not None
        console.print(f"[green][OK][/green] Exported: {out_path}")
        if signed:
            console.print("[green][OK][/green] Signed with Ed25519 key")
        else:
            console.print("[yellow][!][/yellow] Unsigned (run `epi keys generate` to enable signing)")
    except Exception as exc:
        console.print(f"[red][FAIL][/red] Export failed: {exc}")
        return None

    console.print()
    console.print("[dim]Running: epi verify refund_case.epi[/dim]")
    console.print()

    result = subprocess.run(
        [sys.executable, "-m", "epi_cli", "verify", str(out_path)],
        capture_output=False,     # let output flow to terminal
        timeout=15,
    )

    return out_path if result.returncode == 0 else None


